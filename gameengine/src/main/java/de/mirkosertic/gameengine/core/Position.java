package de.mirkosertic.gameengine.core;

public class Position {

    private float x;
    private float y;

    public Position() {
    }

    public Position(float aX, float aY) {
        x = aX;
        y = aY;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}